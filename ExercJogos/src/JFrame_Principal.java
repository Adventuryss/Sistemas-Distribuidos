    void paraCima() {
        do {
            try {
                this.jButton_cobrinha.setBounds(this.jButton_cobrinha.getX(),
                        this.jButton_cobrinha.getY() - 10,
                        this.jButton_cobrinha.getBounds().width, this.jButton_cobrinha.getBounds().width);
                pegou();
                //perseguir
                 if (!listaCauda.isEmpty()) {
                    for (int i = 0; i < listaCauda.size(); i++) {
                        if (i == 0) {
                            ((JButton) listaCauda.get(0)).setBounds(jButton_cobrinha.getX(), jButton_cobrinha.getY() + jButton_cobrinha.getHeight(), jButton_cobrinha.getBounds().width, jButton_cobrinha.getBounds().height);
                        } else {
                            ((JButton) listaCauda.get(i)).setBounds(((JButton) listaCauda.get(i - 1)).getX(), ((JButton) listaCauda.get(i - 1)).getY() + jButton_cobrinha.getHeight() , jButton_cobrinha.getBounds().width, jButton_cobrinha.getBounds().height);
                        }
                    }
                }
                Thread.sleep(VELOCIDADE);

                if (this.jButton_cobrinha.getY() <= 10) {
                    break;
                }
            } catch (InterruptedException ex) {
                Logger.getLogger(JFrame_Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
        } while (true);
    }

