    private void jButton_cobrinhaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jButton_cobrinhaKeyPressed
        // TODO add your handling code here:
        switch (evt.getKeyCode()) {
            case KeyEvent.VK_UP:
                if (threadBotao2.isAlive()) {
                    threadBotao2.suspend();
                }
                threadBotao2 = new Thread() {
                    public void run() {
                        paraCima1();
                    }
                };
                threadBotao2.start();
                break;

            case KeyEvent.VK_DOWN:
                if (threadBotao2.isAlive()) {
                    threadBotao2.suspend();
                    //System.out.println("suspendendo a thread");
                }
                threadBotao2 = new Thread() {
                    public void run() {
                        paraBaixo1();
                    }
                };
                threadBotao2.start();
                break;

            case KeyEvent.VK_LEFT:
                if (threadBotao2.isAlive()) {
                    threadBotao2.suspend();
                    //System.out.println("suspendendo a thread");
                }
                threadBotao2 = new Thread() {
                    public void run() {
                        paraEsquerda1();
                    }
                };
                threadBotao2.start();
                break;

            case KeyEvent.VK_RIGHT:
                if (threadBotao2.isAlive()) {
                    threadBotao2.suspend();
                    //System.out.println("suspendendo a thread");
                }
                threadBotao2 = new Thread() {
                    public void run() {
                        paraDireita1();
                    }
                };
                threadBotao2.start();
                break;
        }
        
        switch (evt.getKeyCode()) {
            case KeyEvent.VK_W:
                if (threadBotao.isAlive()) {
                    threadBotao.suspend();
                }
                threadBotao = new Thread() {
                    public void run() {
                        paraCima();
                    }
                };
                threadBotao.start();
                break;

            case KeyEvent.VK_S:
                if (threadBotao.isAlive()) {
                    threadBotao.suspend();
                    //System.out.println("suspendendo a thread");
                }
                threadBotao = new Thread() {
                    public void run() {
                        paraBaixo();
                    }
                };
                threadBotao.start();
                break;

            case KeyEvent.VK_A:
                if (threadBotao.isAlive()) {
                    threadBotao.suspend();
                    //System.out.println("suspendendo a thread");
                }
                threadBotao = new Thread() {
                    public void run() {
                        paraEsquerda();
                    }
                };
                threadBotao.start();
                break;

            case KeyEvent.VK_D:
                if (threadBotao.isAlive()) {
                    threadBotao.suspend();
                    //System.out.println("suspendendo a thread");
                }
                threadBotao = new Thread() {
                    public void run() {
                        paraDireita();
                    }
                };
                threadBotao.start();
                break;
        }
    }//GEN-LAST:event_jButton_cobrinhaKeyPressed

